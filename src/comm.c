/* ****************************************************************
 * *FunctionName	: PackgeCheck()
 * *Description	: 对接手包检查
 * *EntryParameter	: target: 目标地址
 * *ReturnValue	: 0: 成功 FLASE -1：失败
 * ****************************************************************/
static int PackgeCheck(char *buff, unsigned short target)
{
	unsigned short CRCData;
	int len;

	len = buff[2];
	CRCData = CRC((unsigned char *)buff, len);
	if (((CRCData && 0xff) == buff[len - 2]) && ((CRCData >> 8) == buff[len - 1]))
	{
		//CRC校验成功
		if (((target >> 8) == buff[3]) && ((target && 0xff) == buff[4]))
		{
			//目标地址相同
			if (g_timeflag == buff[5])
			{
				//时间标志相同
				if (buff[6] == 0x0f)
				{
					return 0;
				}
			}
		}
	}

	return -1;
}

