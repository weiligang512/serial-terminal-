unsigned short CRC(unsigned char *pData, unsigned char len)
{
	unsigned char  uchCRCHi = 0xff;
 	unsigned char  uchCRCLo = 0xff;
 	unsigned short uIndex;
 	
 	while(len--)
 	{
 		uIndex = uchCRCHi ^ *pData++;
 		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
		uchCRCLo = auchCRCLo[uIndex];
 	}
 	return ((uchCRCHi*0x100)|uchCRCLo);
}
/* ****************************************************************
 * *FunctionName	: SendRecive()
 * *Description	: 发送包和接收包
 * *EntryParameter	: s: 发送缓冲 r: 接收缓冲
 * *ReturnValue	: 0: 成功 -1：失败
 * ****************************************************************/
static int SendRecive(char *s, char *r)
{
	int len;
	int count;

	len = 0;
	count = 0;
SendAgain:
	len += PortSend(&s[len], s[2] - len);
	if (len < s[2])
	{
		count++;
		if (count > 10)
		{
			fprintf(stderr, "PortSend failed in SendRecive\n");
		}
		goto SendAgain;

		return -1;
	}

	len = 0;
	count = 0;
ReciveAgain:
	len += PortRecive(&r[len], 32);
	if (len >= 3)
	{
		if (r[0] == 0x55 && r[1] == 0xaa)
		{
			if (len == r[2])
			{
				return 0;
			}
			else
			{	
				count++;
				if (count > 10)
				{
					fprintf(stderr, "Recive Package Time Out\n");
					return -1;
				}
				goto ReciveAgain;
			}
		}
	}
	else
	{
		count++;
		if (count > 10)
		{
			fprintf(stderr, "Recive Package Time Out\n");
			return -1;
		}
		goto ReciveAgain;
	}
}


